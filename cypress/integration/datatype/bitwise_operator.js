// &	Bitwise AND	(10==20 & 20==33) = false
// |	Bitwise OR	(10==20 | 20==33) = false
// ^	Bitwise XOR	(10==20 ^ 20==33) = false
// ~	Bitwise NOT	(~10) = -10
// <<	Bitwise Left Shift	(10<<2) = 40
// >>	Bitwise Right Shift	(10>>2) = 2
// >>>	Bitwise Right Shift with Zero	(10>>>2) = 2


console.log(10==20 & 20==33);

console.log(10==20 | 20==33);
console.log(10==20 ^ 20==33);
console.log(~10);
console.log(10<<2);
console.log(10>>2);
