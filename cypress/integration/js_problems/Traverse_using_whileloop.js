// declare and initialize an array with integer values  
var integerArray = [34, 67, 12, 89, 45, 79];   
var i=0;  
  
console.log("Elements in array: <br>");  
//traverse the array using while loop  
while( i< integerArray.length) {  
   //display the array elements  
   console.log(integerArray[i] + "<br>");  
   i++;  
}  
//In this example, we will use a while loop to traverse an array and then display the elements present in this array.

